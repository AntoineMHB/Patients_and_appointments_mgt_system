    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //HERE WE DISPLAY THE APPOINTMENTS
        String speciality = jTextField1.getText();

    try {
        conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/patients_and_appointments_mgt_db", "postgres", "1234");
        String query = "SELECT full_name, date_of_birth, gender, email_or_phone, medical_id, doctor_speciality, " +
                "appointment_date, appointment_time " + // Removed the comma after 'appointment_time'
                "FROM appointments " +
                "WHERE doctor_speciality = ?";
        pst = conn.prepareStatement(query);
        pst.setString(1, speciality);
        rs = pst.executeQuery();

        ResultSetMetaData rsmd = rs.getMetaData();
        int n = rsmd.getColumnCount();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        while (rs.next()) {
            Vector v = new Vector();
            for (int i = 1; i <= n; i++) {
                v.add(rs.getString("full_name"));
                v.add(rs.getString("date_of_birth"));
                v.add(rs.getString("gender"));
                v.add(rs.getString("email_or_phone"));
                v.add(rs.getString("medical_id"));
                v.add(rs.getString("doctor_speciality"));
                v.add(rs.getString("appointment_date"));
                v.add(rs.getString("appointment_time"));
            }
            dtm.addRow(v);
        }

        // Move the JOptionPane outside the while loop
        JOptionPane.showMessageDialog(this, "The appointments have been found");

    } catch (Exception ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed


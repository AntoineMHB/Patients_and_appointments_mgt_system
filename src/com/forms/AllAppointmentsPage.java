    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //HERE WE DELETE AN APPOINTMENT
      String deletingId = deletingIdTxt.getText();
    
    try {
        conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/patients_and_appointments_mgt_db", "postgres", "1234");
        String query = "DELETE FROM appointments WHERE medical_id = ?";
                     
        pst = conn.prepareStatement(query);
        pst.setString(1, deletingId);
        
        int rowsAffected = pst.executeUpdate(); // Use executeUpdate for DELETE operations
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Appointment deleted with success");
        } else {
            JOptionPane.showMessageDialog(this, "No appointment found with that ID");
        }
    } catch(Exception ex) {
        ex.printStackTrace();
    } finally {
        try {
            // Close resources in a finally block to ensure they're closed even if an exception occurs
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //HERE WE WANT TO DISPLAY ALL THE PATIENTS MEDICAL RECORDS
        String patientMedicalId = patientMedicalIdTxt.getText();
//        java.util.Date utilAppointmentDate = appointmentdateTxt.getDate();
//       java.sql.Date sqlAppointmentDate = new java.sql.Date(utilAppointmentDate.getTime());

        try{
            Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/patients_and_appointments_mgt_db", "postgres", "1234");
   String query = "SELECT full_name, medical_id, allergies, past_medical_conditions, family_medical_history, " +
               "immunization_history, prescriptions, blood_pressure, heart_rate, temperature, weight_and_height, " +
               "bmi, labo_results, appointment_date " +
               "FROM medicalrecords " +
               "WHERE medical_id = ?";


            pst = conn.prepareStatement(query);
            pst.setString(1, patientMedicalId);
            //pst.setDate(2, sqlAppointmentDate);
            rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int n = rsmd.getColumnCount();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Vector v = new Vector();
                for(int i=1; i<=n;i++){
                    v.add(rs.getString("full_name"));
                    v.add(rs.getString("medical_id"));
                    v.add(rs.getString("allergies"));
                    v.add(rs.getString("past_medical_conditions"));
                    v.add(rs.getString("family_medical_history"));
                    v.add(rs.getString("immunization_history"));
                    v.add(rs.getString("prescriptions"));
                    v.add(rs.getString("blood_pressure"));
                    v.add(rs.getString("heart_rate"));
                    v.add(rs.getString("temperature"));
                    v.add(rs.getString("weight_and_height"));
                    v.add(rs.getString("bmi"));
                    v.add(rs.getString("labo_results"));
                    v.add(rs.getString("appointment_date"));
                }
                dtm.addRow(v);
                JOptionPane.showMessageDialog(this, "The records have been found");
            }

        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

